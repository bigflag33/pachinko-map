def make_cities_js(cities_raw):
    lines = ["const CITIES = ["]
    for row in cities_raw:
        n, la, lo, p10, p15, p20, a, y0, ya, wa, s, m = row
        lines.append(
            f'  {{n:"{n}", la:{la},lo:{lo},p10:{p10},p15:{p15},p20:{p20},a:{a},'
            f'y0:{y0},ya:{ya},wa:{wa},s:{s},m:{m}}},'
        )
    lines.append("];")
    return "\n".join(lines)

