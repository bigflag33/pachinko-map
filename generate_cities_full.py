GSI_URL = "https://msearch.gsi.go.jp/address-search/AddressSearch?q={}"

def geocode(name, pref):
    """国土地理院APIで住所→緯度経度（日本範囲チェック付き・リトライ付き）"""
    query = urllib.parse.quote(pref + name)
    for attempt in range(3):
        try:
            r = requests.get(GSI_URL.format(query), timeout=10)
            data = r.json()
            if data:
                c = data[0]["geometry"]["coordinates"]
                lat, lng = float(c[1]), float(c[0])
                # 日本国内チェック (lat: 24-46, lng: 122-154)
                if 24.0 <= lat <= 46.5 and 122.0 <= lng <= 154.0:
                    return lat, lng
        except Exception:
            pass
        time.sleep(0.5 * (attempt + 1))
    return None, None

def est_pop(pop20, decline=0.02):
    """2020人口から2010/2015を推定"""
    p15 = int(pop20 / (1 - decline))
    p10 = int(p15 / (1 - decline))
    return p10, p15

def main():
    results = []
    total = len(ALL_CITIES)
    print(f"全{total}市区町村を処理します（約{total//10}秒）")

    seen_coords = set()
    for i, (name, pref, pop20, area) in enumerate(ALL_CITIES, 1):
        lat, lng = geocode(name, pref)
        if lat is None:
            print(f"  [{i}/{total}] {name} ({pref}) → geocode失敗、スキップ")
            continue

        # 重複座標チェック（同じ座標が既に登録されていたらスキップ）
        coord_key = (round(lat, 3), round(lng, 3))
        if coord_key in seen_coords:
            print(f"  [{i}/{total}] {name} ({pref}) → 重複座標、スキップ")
            continue
        seen_coords.add(coord_key)

        p10, p15 = est_pop(pop20)
        y0, ya, wa, s, m = PREF_AGE.get(pref, (11.0, 24.5, 34.5, 30.0, 48.5))

        results.append((name, lat, lng, p10, p15, pop20, area, y0, ya, wa, s, m))
        if i % 20 == 0 or i == total:
            print(f"  [{i}/{total}] {name} ({pref}) → ({lat:.4f}, {lng:.4f})")
        time.sleep(0.3)

    # CITIES JS生成
    lines = ["const CITIES = ["]
    for row in results:
        n, la, lo, p10, p15, p20, a, y0, ya, wa, s, m = row
        lines.append(f'  {{n:"{n}", la:{la},lo:{lo},p10:{p10},p15:{p15},p20:{p20},a:{a},y0:{y0},ya:{ya},wa:{wa},s:{s},m:{m}}},')
    lines.append("];")
    js = "\n".join(lines)

    # template_v5.html更新
    template = pathlib.Path(__file__).parent / "template_v5.html"
    html = template.read_text(encoding="utf-8")
    pattern = re.compile(r"const CITIES = \[.*?\];", re.DOTALL)
    if pattern.search(html):
        new_html = pattern.sub(js, html)
        template.write_text(new_html, encoding="utf-8")
        print(f"\n✅ 完了: {len(results)}市区町村 → template_v5.html 更新")
    else:
        out = pathlib.Path(__file__).parent / "cities_full.js"
        out.write_text(js, encoding="utf-8")
        print(f"\n✅ cities_full.js に出力しました（{len(results)}件）")

if __name__ == "__main__":
    main()
